AUTHOR_NAME = "D.ryu"


def generate_book(topic: str, category: str, client: Groq) -> dict:
    author = AUTHOR_NAME
    kdp_category = KDP_CATEGORIES.get(category, KDP_CATEGORIES["default"])

    prompt = f"""あなたはAmazon Kindle向け日本語実用書の専門ライターです。
以下のテーマで、実際に売れる高品質な電子書籍を執筆してください。

テーマ: {topic}

## 品質要件
- 日本語で書く
- 読者は初心者〜中級者の日本人
- 各章は必ず1200文字以上（具体的な数字・事例・手順を含める）
- 「〜です・ます」調で親しみやすく
- 読んだらすぐ実践できる具体的なアドバイス
- 章ごとに「ポイント」や「実践ステップ」を含める
- 合計10章構成

## Amazon SEO要件
- タイトルには検索されるキーワードを必ず含める
- 説明文は400文字以上、読者の悩みに共感→解決策→行動を促す構成

## 出力形式（JSONのみ・余分なテキスト不要）
{{
  "title": "SEO最適化されたタイトル（元のテーマをベースに魅力的に）",
  "subtitle": "読者の悩みを解決することを示すサブタイトル",
  "description": "Amazon販売ページ用の説明文（400文字以上）。読者の悩みへの共感→本書で解決できること→読後の変化の順で書く",
  "author": "{author}",
  "kdp_category": "{kdp_category}",
  "chapters": [
    {{
      "title": "第1章: タイトル",
      "content": "本文（1200文字以上。具体的な数字・事例・ステップを含む）"
    }}
  ]
}}

必ず10章分すべて出力すること。"""

    for attempt in range(3):
        try:
            resp = client.chat.completions.create(
                model="llama-3.3-70b-versatile",
                messages=[{"role": "user", "content": prompt}],
                temperature=0.7,
                max_tokens=8000,
            )
            text = resp.choices[0].message.content.strip()
            import re
            m = re.search(r"\{[\s\S]+\}", text)
            if m:
                data = json.loads(m.group())
                data["author"] = author
                return data
        except Exception as e:
            if attempt < 2:
                print(f"  リトライ... ({e})")
                time.sleep(10)
            else:
                raise
    raise RuntimeError("コンテンツ生成失敗")


def pick_next_topic(done_topics: list) -> tuple | None:
    for topic_data in BOOK_TOPICS:
        if topic_data[0] not in done_topics:
            return topic_data
    return None
